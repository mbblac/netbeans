public class Escotistas {
    private int reg_escotista;
    private String senha_escotista, nome_escotista, cargo_escotista, ramo_escotista;

    public int getreg_escotista() {
        return reg_escotista;
    }

    public void setreg_escotista(int reg_escotista) {
        this.reg_escotista = reg_escotista;
    }

    public String getsenha_escotista() {
        return senha_escotista;
    }

    public void setsenha_escotista(String senha_escotista) {
        this.senha_escotista = senha_escotista;
    }

    public String getnome_escotista() {
        return nome_escotista;
    }

    public void setnome_escotista(String nome_escotista) {
        this.nome_escotista = nome_escotista;
    }

    public String getcargo_escotista() {
        return cargo_escotista;
    }

    public void setcargo_escotista(String cargo_escotista) {
        this.cargo_escotista = cargo_escotista;
    }

    public String getramo_escotista() {
        return ramo_escotista;
    }

    public void setramo_escotista(String ramo_escotista) {
        this.ramo_escotista = ramo_escotista;
    }

    
   
    
    
}
